package capaModeloCC;

public class FormacionCandidato {
    private String institucion;
    private String programa;
    private String nivel;
    private String estado;
    private String modalidad;
    private String jornada;
    private String periodicidad_academica;
    private String periodo_actual;

    // Getters y Setters

    public String getInstitucion() {
        return institucion;
    }

    public void setInstitucion(String institucion) {
        this.institucion = institucion;
    }

    public String getPrograma() {
        return programa;
    }

    public void setPrograma(String programa) {
        this.programa = programa;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getModalidad() {
        return modalidad;
    }

    public void setModalidad(String modalidad) {
        this.modalidad = modalidad;
    }

    public String getJornada() {
        return jornada;
    }

    public void setJornada(String jornada) {
        this.jornada = jornada;
    }

    public String getPeriodicidadAcademica() {
        return periodicidad_academica;
    }

    public void setPeriodicidadAcademica(String periodicidadAcademica) {
        this.periodicidad_academica = periodicidadAcademica;
    }

    public String getPeriodoActual() {
        return periodo_actual;
    }

    public void setPeriodoActual(String periodoActual) {
        this.periodo_actual = periodoActual;
    }
}
